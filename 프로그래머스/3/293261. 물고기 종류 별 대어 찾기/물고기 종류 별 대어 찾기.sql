WITH MAX_TABLE AS (
    SELECT FISH_TYPE, MAX(LENGTH) AS LENGTH
    FROM FISH_INFO
    GROUP BY FISH_TYPE
)

SELECT F.ID, N.FISH_NAME, F.LENGTH
FROM FISH_INFO F
JOIN MAX_TABLE FF ON F.FISH_TYPE = FF.FISH_TYPE AND F.LENGTH = FF.LENGTH
JOIN FISH_NAME_INFO N ON F.FISH_TYPE = N.FISH_TYPE
ORDER BY ID ASC;